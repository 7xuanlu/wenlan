#!/usr/bin/env bash
# Test bump-version.sh syncs all manifests from version.txt
set -euo pipefail

# Setup: temp workspace mimicking real layout
# Use ${TMPDIR:-/tmp} so this works both inside Claude Code sandbox and in CI.
TMPDIR_TEST=$(mktemp -d "${TMPDIR:-/tmp}/bump-version-test.XXXXXX")
trap "rm -rf $TMPDIR_TEST" EXIT

mkdir -p "$TMPDIR_TEST/crates/wenlan-mcp/npm"
mkdir -p "$TMPDIR_TEST/crates/wenlan-cli/npm"
mkdir -p "$TMPDIR_TEST/plugin/.claude-plugin"
mkdir -p "$TMPDIR_TEST/plugin/bin"
mkdir -p "$TMPDIR_TEST/plugin/skills/setup"

cat > "$TMPDIR_TEST/version.txt" <<EOF
0.5.0
EOF

cat > "$TMPDIR_TEST/Cargo.toml" <<EOF
[workspace.package]
version = "0.4.1"   # x-release-please-version

[workspace.dependencies]
wenlan-types = { path = "crates/wenlan-types", version = "0.4.1" }
wenlan-core  = { path = "crates/wenlan-core",  version = "0.4.1" }
EOF

cat > "$TMPDIR_TEST/crates/wenlan-mcp/npm/package.json" <<EOF
{"name": "wenlan-mcp", "version": "0.4.1"}
EOF

cat > "$TMPDIR_TEST/crates/wenlan-cli/npm/package.json" <<EOF
{"name": "wenlan", "version": "0.4.1"}
EOF

cat > "$TMPDIR_TEST/plugin/.claude-plugin/plugin.json" <<EOF
{"name": "wenlan", "version": "0.4.1"}
EOF

cat > "$TMPDIR_TEST/plugin/bin/wenlan-mcp-runner.sh" <<EOF
exec npx -y wenlan-mcp@^0.4.1 "\$@"
EOF

cat > "$TMPDIR_TEST/plugin/skills/setup/SKILL.md" <<EOF
Bash: curl -fsSL https://raw.githubusercontent.com/7xuanlu/wenlan/v0.4.1/install.sh | bash
EOF

# Cargo.lock fixture: workspace members at the old version + an external dep
# (anyhow) that must NOT be touched + the top-level lockfile-format version.
cat > "$TMPDIR_TEST/Cargo.lock" <<EOF
# This file is automatically @generated by Cargo.
# It is highly recommended to commit this file to your version control system.

version = 3

[[package]]
name = "anyhow"
version = "1.0.86"
source = "registry+https://github.com/rust-lang/crates.io-index"

[[package]]
name = "wenlan"
version = "0.4.1"
dependencies = [
 "anyhow",
 "wenlan-core",
 "wenlan-types",
]

[[package]]
name = "wenlan-core"
version = "0.4.1"
dependencies = [
 "wenlan-types",
]

[[package]]
name = "wenlan-mcp"
version = "0.4.1"

[[package]]
name = "wenlan-server"
version = "0.4.1"

[[package]]
name = "wenlan-types"
version = "0.4.1"
EOF

# Run script in temp dir
(cd "$TMPDIR_TEST" && bash "$OLDPWD/scripts/bump-version.sh")

# Assert all manifests bumped to 0.5.0
WS_VER=$(grep -E '^version = ' "$TMPDIR_TEST/Cargo.toml" | sed -E 's/version = "([^"]+)".*/\1/')
WENLAN_TYPES_DEP_VER=$(grep -E '^wenlan-types[[:space:]]+=' "$TMPDIR_TEST/Cargo.toml" | sed -E 's/.*version = "([^"]+)".*/\1/')
WENLAN_CORE_DEP_VER=$(grep -E '^wenlan-core[[:space:]]+=' "$TMPDIR_TEST/Cargo.toml" | sed -E 's/.*version = "([^"]+)".*/\1/')
MCP_NPM_VER=$(jq -r .version "$TMPDIR_TEST/crates/wenlan-mcp/npm/package.json")
WENLAN_NPM_VER=$(jq -r .version "$TMPDIR_TEST/crates/wenlan-cli/npm/package.json")
PLUGIN_VER=$(jq -r .version "$TMPDIR_TEST/plugin/.claude-plugin/plugin.json")

[[ "$WS_VER" == "0.5.0" ]]   || { echo "FAIL: Cargo.toml not bumped (got $WS_VER)"; exit 1; }
[[ "$WENLAN_TYPES_DEP_VER" == "0.5.0" ]] || { echo "FAIL: wenlan-types dep not bumped (got $WENLAN_TYPES_DEP_VER)"; exit 1; }
[[ "$WENLAN_CORE_DEP_VER" == "0.5.0" ]] || { echo "FAIL: wenlan-core dep not bumped (got $WENLAN_CORE_DEP_VER)"; exit 1; }
[[ "$MCP_NPM_VER" == "0.5.0" ]]  || { echo "FAIL: wenlan-mcp npm not bumped (got $MCP_NPM_VER)"; exit 1; }
[[ "$WENLAN_NPM_VER" == "0.5.0" ]]  || { echo "FAIL: wenlan npm not bumped (got $WENLAN_NPM_VER)"; exit 1; }
[[ "$PLUGIN_VER" == "0.5.0" ]] || { echo "FAIL: plugin not bumped (got $PLUGIN_VER)"; exit 1; }
grep -q 'wenlan-mcp@\^0.5.0' "$TMPDIR_TEST/plugin/bin/wenlan-mcp-runner.sh" || { echo "FAIL: runner pin not bumped"; exit 1; }
grep -q '/v0.5.0/install.sh' "$TMPDIR_TEST/plugin/skills/setup/SKILL.md" || { echo "FAIL: setup skill installer not bumped"; exit 1; }

# Cargo.lock: all five workspace members bumped to 0.5.0, exactly as
# validate-versions.sh reads them (name:version pairs).
LOCK_VERSIONS=$(awk '
  $0 == "[[package]]" { in_pkg=1; name=""; version=""; next }
  in_pkg && $1 == "name" && $2 == "=" { name=$3; gsub(/"/, "", name); next }
  in_pkg && $1 == "version" && $2 == "=" {
    version=$3; gsub(/"/, "", version)
    if (name == "wenlan" || name == "wenlan-core" || name == "wenlan-mcp" || name == "wenlan-server" || name == "wenlan-types") print name ":" version
    in_pkg=0
  }
' "$TMPDIR_TEST/Cargo.lock" | sort)
for crate in wenlan wenlan-core wenlan-mcp wenlan-server wenlan-types; do
  printf '%s\n' "$LOCK_VERSIONS" | grep -qx "${crate}:0.5.0" || { echo "FAIL: Cargo.lock ${crate} not bumped to 0.5.0 (got: $(printf '%s' "$LOCK_VERSIONS" | grep "^${crate}:" || echo none))"; exit 1; }
done
# External deps must be left alone.
grep -q '^version = "1.0.86"' "$TMPDIR_TEST/Cargo.lock" || { echo "FAIL: external dep anyhow version was altered"; exit 1; }
# Top-level lockfile-format version must be left alone.
grep -q '^version = 3$' "$TMPDIR_TEST/Cargo.lock" || { echo "FAIL: lockfile-format 'version = 3' was altered"; exit 1; }

echo "PASS: bump-version.sh syncs all manifests"
